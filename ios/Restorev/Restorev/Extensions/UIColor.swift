import UIKit

extension UIColor {

  @nonobjc class var backgroundWhite: UIColor {
    return UIColor(red: 247.0 / 255.0, green: 245.0 / 255.0, blue: 249.0 / 255.0, alpha: 1.0)
  }

  @nonobjc class var brandBlue: UIColor {
    return UIColor(red: 28.0 / 255.0, green: 67.0 / 255.0, blue: 126.0 / 255.0, alpha: 1.0)
  }

  @nonobjc class var neutralBlack: UIColor {
    return UIColor(white: 51.0 / 255.0, alpha: 1.0)
  }

  @nonobjc class var alphaBlue: UIColor {
    return UIColor(red: 219.0 / 255.0, green: 225.0 / 255.0, blue: 233.0 / 255.0, alpha: 1.0)
  }

  @nonobjc class var dangerRed: UIColor {
    return UIColor(red: 237.0 / 255.0, green: 23.0 / 255.0, blue: 36.0 / 255.0, alpha: 1.0)
  }

}
